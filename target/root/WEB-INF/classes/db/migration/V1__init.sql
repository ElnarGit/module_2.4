create table users(
    id int generated by default as identity PRIMARY KEY,
    name varchar(100) not null
);

create table files(
    id int generated by default as identity PRIMARY KEY,
    name varchar(100) not null,
    file_path varchar(100) not null
);

create table events(
    id int generated by default as identity PRIMARY KEY,
    user_id int references users(id),
    file_id int references files(id)
);

